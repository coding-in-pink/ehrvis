(function ($) {
    var options = {
        shift: {
            interactive: false,
            cursor: "move",
            frameRate: 20
        }
    };

    var range = null;

    function init(plot) {

        var prevCursor = 'default', prevPageX = 0, prevPageY = 0,
            shiftTimeout = null;

        function onDragStart(e) {
            if (e.which != 1)  // only accept left-click
                return false;
            var c = plot.getPlaceholder().css('cursor');
            if (c)
                prevCursor = c;
            plot.getPlaceholder().css('cursor', plot.getOptions().shift.cursor);
            prevPageX = e.pageX;
            prevPageY = e.pageY;
        }
        
        function onDrag(e) {
            var frameRate = plot.getOptions().shift.frameRate;
            if (shiftTimeout || !frameRate)
                return;

            shiftTimeout = setTimeout(function () {
                plot.shift({ left: prevPageX - e.pageX,
                           top: prevPageY - e.pageY });
                prevPageX = e.pageX;
                prevPageY = e.pageY;
                                                    
                shiftTimeout = null;
            }, 1 / frameRate * 1000);
        }

        function onDragEnd(e) {
            if (shiftTimeout) {
                clearTimeout(shiftTimeout);
                shiftTimeout = null;
            }
                    
            plot.getPlaceholder().css('cursor', prevCursor);
            plot.shift({ left: prevPageX - e.pageX,
                       top: prevPageY - e.pageY });
        }
        
        function bindEvents(plot, eventHolder) {
            var o = plot.getOptions();
            if (o.shift.interactive) {
                eventHolder.bind("dragstart", { distance: 10 }, onDragStart);
                eventHolder.bind("drag", onDrag);
                eventHolder.bind("dragend", onDragEnd);
            }
        }

        plot.shift = function (args) {
            var delta = {
                x: +args.left,
                y: +args.top
            };

            if (isNaN(delta.x))
                delta.x = 0;
            if (isNaN(delta.y))
                delta.y = 0;

            
            var selection = plot.getSelection();

            if (selection == null) 
                return;

            var ranges = { 
                xaxis: 
                    { from: selection.xaxis.from, 
                        to: selection.xaxis.to
                    }, 
                yaxis: 
                    { from: selection.yaxis.from,  
                        to: selection.yaxis.to
                    } 
                }

            axis = plot.getAxes().xaxis;
            var shift = -axis.c2p(delta.x) + axis.min;

            ranges.xaxis.to += shift;
            ranges.xaxis.from += shift;

            // in the case that it exceeds the window frame
            var sliderwidth = ranges.xaxis.to - ranges.xaxis.from;

            if (ranges.xaxis.to > axis.max) { // exceeds maximum
                ranges.xaxis.to = axis.max;
                ranges.xaxis.from = axis.max - sliderwidth;
            }
            if (ranges.xaxis.from < axis.min) { // exceeds minimum
                ranges.xaxis.from = axis.min;
                ranges.xaxis.to = axis.min + sliderwidth;
            }

            plot.range = ranges;
            
            if (!args.preventEvent)
                plot.getPlaceholder().trigger("plotshift", [ plot, args ]);
        };

        function shutdown(plot, eventHolder) {
            eventHolder.unbind("dragstart", onDragStart);
            eventHolder.unbind("drag", onDrag);
            eventHolder.unbind("dragend", onDragEnd);
            if (shiftTimeout)
                clearTimeout(shiftTimeout);
        }
        
        plot.getNewSelection = function(){
            return plot.range;
        };

        plot.hooks.bindEvents.push(bindEvents);
        plot.hooks.shutdown.push(shutdown);
    }
    
    $.plot.plugins.push({
        init: init,
        options: options,
        name: 'shift',
        version: '1.3'
    });
})(jQuery);